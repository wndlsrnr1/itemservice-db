import static org.assertj.core.api.Assertions.assertThat;

@SpringBootTest
@Transactional
@Slf4j
public class ItemRepositoryTest2 {

    @Autowired
    ItemRepositoryV2 itemRepository;
    @Autowired
    ItemQueryRepositoryV2 itemQueryRepositoryV2;


    @AfterEach
    void afterEach() {
        if (itemRepository instanceof MemoryItemRepository) {
            ((MemoryItemRepository) itemRepository).clearStore();
        }
    }

    @Test
    void save() {
        //given
        Item item = new Item("itemA", 10000, 10);

        //when
        Item savedItem = itemRepository.save(item);

        //then
        Item findItem = itemRepository.findById(item.getId()).get();
        assertThat(findItem).isEqualTo(savedItem);
    }

    @Test
    void updateItem() {
        //given
        Item item = new Item("item1", 10000, 10);
        Item savedItem = itemRepository.save(item);
        Long itemId = savedItem.getId();

        //when
        Item updateParam = new Item("item2", 20000, 30);
        item.setItemName("item2");
        item.setPrice(20000);
        item.setQuantity(30);

        //then
        Item findItem = itemRepository.findById(itemId).get();
        log.info("findItem = {}", findItem.getItemName());
        assertThat(findItem.getItemName()).isEqualTo(updateParam.getItemName());
        assertThat(findItem.getPrice()).isEqualTo(updateParam.getPrice());
        assertThat(findItem.getQuantity()).isEqualTo(updateParam.getQuantity());
    }

    //개발환경과 테스트 환경을 분리 하지 않아서 오류가 생김

    @Test
    void findItemsZero() {
        //given
        Item item1 = new Item("itemA-1", 10000, 10);
        Item item2 = new Item("itemA-2", 20000, 20);
        Item item3 = new Item("itemB-1", 30000, 30);

        itemRepository.save(item1);
        itemRepository.save(item2);
        itemRepository.save(item3);
        ItemSearchCond itemSearchCond = new ItemSearchCond("itemA", 10000);
        log.info("findAll = {}", itemQueryRepositoryV2.findAll(itemSearchCond));
    }

    @Test
    void findItems() {
        //given
        Item item1 = new Item("itemA-1", 10000, 10);
        Item item2 = new Item("itemA-2", 20000, 20);
        Item item3 = new Item("itemB-1", 30000, 30);

        itemRepository.save(item1);
        itemRepository.save(item2);
        itemRepository.save(item3);

        //둘 다 없음 검증
        test(null, null, item1, item2, item3);
        test("", null, item1, item2, item3);

        //itemName 검증
        test("itemA", null, item1, item2);
        test("itemA", null, item1, item2);
        test("itemB", null, item3);

        //maxPrice 검증
        test(null, 10000, item1);

        //둘 다 있음 검증
        test("itemA", 10000, item1);
    }

    void test(String itemName, Integer maxPrice, Item... items) {
        ItemSearchCond itemSearchCond = new ItemSearchCond(itemName, maxPrice);
        List<Item> result = itemQueryRepositoryV2.findAll(itemSearchCond);
        log.info("result = {}", result);
        assertThat(result).containsExactly(items);
    }

}
